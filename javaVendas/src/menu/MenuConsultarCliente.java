    private void limparFormulario(){
        txt_Id.setText("");
        txt_Nome.setText("");
        txt_Endereco.setText("");
        txt_Email.setText("");
        txt_Telefone.setText("");
        }
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int idCliente = Integer.parseInt(txt_Id.getText());
        ClienteDAO cDAO = new ClienteDAO();
        
        Cliente c = cDAO.getCliente(idCliente);
        if(c == null){
            limparFormulario();
            JOptionPane.showMessageDialog(this, "Pessoa não encontrada!");
        }
        else{
            txt_Id.setText(String.valueOf(c.getidCliente()));
            txt_Nome.setText(c.getNomeCliente());
            txt_Endereco.setText(c.getEnderecoCliente());
            txt_Email.setText(c.getEmailCliente());
            txt_Telefone.setText(c.getTelefoneCliente());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

