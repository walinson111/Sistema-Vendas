    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        Cliente c = new Cliente();
        c.setIdCliente(Integer.parseInt(txt_Id.getText()));
        c.setNomeCliente(txt_Nome.getText());
        c.setEnderecoCliente(txt_Endereco.getText());
        c.setEmailCliente(txt_Email.getText());
        c.setTelefoneCliente(txt_Telefone.getText());
        ClienteDAO cDAO = new ClienteDAO();
        cDAO.editar(c);
        
        limparFormulario();
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int resposta = JOptionPane.showConfirmDialog(null, "Deseja realmente excluir essa pessoa?",
        "Exclusão", JOptionPane.YES_NO_OPTION);
        
        if(resposta == JOptionPane.YES_OPTION){
            ClienteDAO cDAO = new ClienteDAO();
            cDAO.deletar(Integer.parseInt(txt_Id.getText()));
            
            limparFormulario();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

